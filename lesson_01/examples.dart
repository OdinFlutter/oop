class Driver {
  // –í–ª–∞—Å—Ç–∏–≤–æ—Å—Ç—ñ (–ø–æ–ª—è) –∫–ª–∞—Å—É
  String name; // –Ü–º'—è –≥–æ–Ω—â–∏–∫–∞
  int age; // –í—ñ–∫ –≥–æ–Ω—â–∏–∫–∞
  String country; // –ö—Ä–∞—ó–Ω–∞ –≥–æ–Ω—â–∏–∫–∞
  int points; // –û—á–∫–∏ –≤ —á–µ–º–ø—ñ–æ–Ω–∞—Ç—ñ

  // –û—Å–Ω–æ–≤–Ω–∏–π –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä
  Driver(this.name, this.age, this.country) : points = 0;

  // –ù–∞–∑–≤–∞–Ω–∏–π –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –¥–ª—è –ª–µ–≥–µ–Ω–¥ (–∞–≤—Ç–æ–º–∞—Ç–∏—á–Ω–æ 40 —Ä–æ–∫—ñ–≤)
  Driver.legend(this.name, this.country) : age = 40, points = 0;

  // –ú–µ—Ç–æ–¥ –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è —ñ–Ω—Ñ–æ—Ä–º–∞—Ü—ñ—ó –ø—Ä–æ –≥–æ–Ω—â–∏–∫–∞
  void displayInfo() {
    print('üë§ –ì–æ–Ω—â–∏–∫: $name');
    print('   –í—ñ–∫: $age —Ä–æ–∫—ñ–≤');
    print('   –ö—Ä–∞—ó–Ω–∞: $country');
    print('   –û—á–∫–∏: $points');
    print('');
  }

  // –ú–µ—Ç–æ–¥ –¥–ª—è —ó–∑–¥–∏
  void drive() {
    print('üèéÔ∏è  $name —ó–¥–µ –ø–æ —Ç—Ä–∞—Å—ñ –∑ –≤–µ–ª–∏–∫–æ—é —à–≤–∏–¥–∫—ñ—Å—Ç—é!');
  }

  // –ú–µ—Ç–æ–¥ –¥–ª—è –æ–±–≥–æ–Ω—É
  void overtake() {
    print('‚ö° $name –∑–¥—ñ–π—Å–Ω—é—î –±–ª–∏—Å–∫—É—á–∏–π –æ–±–≥—ñ–Ω!');
  }

  // –ú–µ—Ç–æ–¥ –¥–ª—è –¥–æ–¥–∞–≤–∞–Ω–Ω—è –æ—á–æ–∫
  void addPoints(int racePoints) {
    points += racePoints;
    print('üèÜ $name –æ—Ç—Ä–∏–º–∞–≤ $racePoints –æ—á–æ–∫! –ó–∞–≥–∞–ª–æ–º: $points');
  }
}

