// üèéÔ∏è –ö–õ–ê–° F1CAR (–ê–í–¢–û–ú–û–ë–Ü–õ–¨ –§–û–†–ú–£–õ–ò 1)
// =============================================================================

class F1Car {
  // –í–ª–∞—Å—Ç–∏–≤–æ—Å—Ç—ñ –∞–≤—Ç–æ–º–æ–±—ñ–ª—è
  String model; // –ú–æ–¥–µ–ª—å –∞–≤—Ç–æ–º–æ–±—ñ–ª—è
  String team; // –ö–æ–º–∞–Ω–¥–∞
  int maxSpeed; // –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞ —à–≤–∏–¥–∫—ñ—Å—Ç—å (–∫–º/–≥–æ–¥)
  int currentSpeed; // –ü–æ—Ç–æ—á–Ω–∞ —à–≤–∏–¥–∫—ñ—Å—Ç—å
  bool isEngineOn; // –ß–∏ –∑–∞–ø—É—â–µ–Ω–∏–π –¥–≤–∏–≥—É–Ω

  // –û—Å–Ω–æ–≤–Ω–∏–π –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä
  F1Car(this.model, this.team, this.maxSpeed)
    : currentSpeed = 0,
      isEngineOn = false;

  // –ù–∞–∑–≤–∞–Ω–∏–π –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –¥–ª—è —Ç–µ—Å—Ç–æ–≤–æ–≥–æ –∞–≤—Ç–æ–º–æ–±—ñ–ª—è
  F1Car.testCar(this.team)
    : model = 'Test Car',
      maxSpeed = 300,
      currentSpeed = 0,
      isEngineOn = false;

  // –ù–∞–∑–≤–∞–Ω–∏–π –∫–æ–Ω—Å—Ç—Ä—É–∫—Ç–æ—Ä –¥–ª—è –∫–ª–∞—Å–∏—á–Ω–æ–≥–æ –∞–≤—Ç–æ–º–æ–±—ñ–ª—è
  F1Car.vintage(this.model, this.team)
    : maxSpeed = 280,
      currentSpeed = 0,
      isEngineOn = false;

  // –ú–µ—Ç–æ–¥ –¥–ª—è –≤—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫
  void displaySpecs() {
    print('üèéÔ∏è  –ê–≤—Ç–æ–º–æ–±—ñ–ª—å: $model');
    print('   –ö–æ–º–∞–Ω–¥–∞: $team');
    print('   –ú–∞–∫—Å. —à–≤–∏–¥–∫—ñ—Å—Ç—å: $maxSpeed –∫–º/–≥–æ–¥');
    print('   –ü–æ—Ç–æ—á–Ω–∞ —à–≤–∏–¥–∫—ñ—Å—Ç—å: $currentSpeed –∫–º/–≥–æ–¥');
    print('   –î–≤–∏–≥—É–Ω: ${isEngineOn ? "–ó–∞–ø—É—â–µ–Ω–∏–π" : "–í–∏–º–∫–Ω–µ–Ω–∏–π"}');
    print('');
  }

  // –ú–µ—Ç–æ–¥ –¥–ª—è –∑–∞–ø—É—Å–∫—É –¥–≤–∏–≥—É–Ω–∞
  void startEngine() {
    isEngineOn = true;
    print('üî• –î–≤–∏–≥—É–Ω $model –∑–∞–ø—É—â–µ–Ω–æ! –ì–æ—Ç–æ–≤–∏–π –¥–æ –≥–æ–Ω–∫–∏!');
  }

  // –ú–µ—Ç–æ–¥ –¥–ª—è –ø—Ä–∏—Å–∫–æ—Ä–µ–Ω–Ω—è
  void accelerate() {
    if (!isEngineOn) {
      startEngine();
    }
    currentSpeed += 50;
    if (currentSpeed > maxSpeed) {
      currentSpeed = maxSpeed;
    }
    print('‚ö° $model –ø—Ä–∏—Å–∫–æ—Ä—é—î—Ç—å—Å—è! –®–≤–∏–¥–∫—ñ—Å—Ç—å: $currentSpeed –∫–º/–≥–æ–¥');
  }

  // –ú–µ—Ç–æ–¥ –¥–ª—è –≥–∞–ª—å–º—É–≤–∞–Ω–Ω—è
  void brake() {
    currentSpeed -= 30;
    if (currentSpeed < 0) {
      currentSpeed = 0;
    }
    print('üõë $model –≥–∞–ª—å–º—É—î! –®–≤–∏–¥–∫—ñ—Å—Ç—å: $currentSpeed –∫–º/–≥–æ–¥');
  }
}
